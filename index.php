<h2>Apa itu algoritma SAW?</h2>
<p>Algoritma SAW(Simple Additive Weighting) adalah salah satu algoritma yang digunakan untuk pengambilan
    keputusan. Algoritma SAW juga dikenal dengan algoritma dengan metode penjumlahan berbobot. Metode ini
    membutuhkan proses normalisasi matrix keputusan (x) ke suatu skala yang dapat dibandingkan dengan semua
    rating alternatif yang ada. Pada artikel kali ini saya akan memberikan contoh penerapan algoritma saw
    untuk pemilihan handphone.</p>

